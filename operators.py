# Multiplication of two numbers.....

number1 = float(input("Enter the first number: "))
number2 = float(input("Enter the second number: "))

result1 = number1 + number2
result2 = number1 - number2
result3 = number1 * number2
result4 = number1 / number2


print(f"The sum of {number1} and {number2} is: {result1}")
print(f"The subtract of {number1} and {number2} is: {result2}")
print(f"The product of {number1} and {number2} is: {result3}")
print(f"The divide of {number1} and {number2} is: {result4}")


